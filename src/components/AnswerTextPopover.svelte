<script>
	/**
	 * @type {{ id: string, text: string }}
	 */
	let { id, text } = $props();

	/** @param {MouseEvent & { currentTarget: HTMLElement }} event */
	function closePopover(event) {
		event.currentTarget.hidePopover();
	}
</script>

<button
	{id}
	class="answer-text-popover"
	type="button"
	popover="auto"
	onclick={closePopover}
>
	{text}
</button>

